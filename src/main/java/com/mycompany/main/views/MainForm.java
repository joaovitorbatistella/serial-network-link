    private static byte[] hexStringToByte (String hex)
    {
        int val = Integer.parseInt(hex, 16);

        BigInteger big = BigInteger.valueOf(val);
        byte[] ans = (big.toByteArray());
        return ans;
    }
        
     private static String addScapes(String content) 
    {
        // enquadramento por utiliza√ß√£o de byte de inicio e fim de quadro com inser√ß√£o de caractere de escape
        char scapeCharacter = '*';
        
        int index = 0;
        for(char ch: content.toCharArray()) {
            if(ch == scapeCharacter) {
                content = addChar(content, scapeCharacter, index);
                index +=2;
            } else {
                index++;
            }
            
        }
        
        return content;
    }
    
    public static String addChar(String str, char ch, int position) {
        int len = str.length();
        char[] updatedArr = new char[len + 1];
        str.getChars(0, position, updatedArr, 0);
        updatedArr[position] = ch;
        str.getChars(position, len, updatedArr, position + 1);
        return new String(updatedArr);
    }

